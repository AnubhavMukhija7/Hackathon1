class Vendor {
    constructor(vendorId, company, facility, name, contact, status, amount) {
        this.VendorID = vendorId;
        this.Company = company;
        this.Facility = facility;
        this.Name = name;
        this.Contact = contact;
        this.ActiveStatus = status;
        this.Amount = amount;
    }
}

export default Vendor;
